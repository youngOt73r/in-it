

class Carc:
    def __init__(self, a, b):
        self.a = a
        self.b = b
        # init when create this class 

    def plus(self):
        print ("연산중입니다.\n")
        return self.a + self.b
        # carcul

    def minus(self):
        print ("연산중입니다.\n")
        return self.a - self.b
        # carcul

    def mul(self):
        print ("연산중입니다.\n")
        return self.a * self.b
        # carcul

    def div(self):
        print ("연산중입니다.\n")
        return self.a / self.b
        # carcul